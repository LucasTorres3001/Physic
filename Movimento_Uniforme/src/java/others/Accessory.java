package others;


public class Accessory {
    
    public static double VelocidadeFinal(double a, double b, double c){
        return Math.sqrt((a * a) + (b * 2 * c));
    }
    
    public static double QntdDeMovimento(double d, double e){
        return d * e;
    }
    
    public static double Forca(double f, double g){
        return f * g;
    }
    
    public static double Peso(double h, double i){
        return h * i;
    }
    
}
